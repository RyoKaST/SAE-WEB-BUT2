        <h2> Conference nationale du handicap </h2>
        <p>Le 26 avril 2023 le Président de la République a conclu la 6ème Conférence Nationale du Handicap sans avoir participé à ses travaux. <br>

            Beaucoup de promesses ont été faites, entre autres :<br><br>
            
            Le diagnostic précoce du spectre autistique pour les enfants et ce, sans reste à charge pour les familles.<br>
            Étendre la prise en charge des enfants scolarisés en situation de handicap, aux temps périscolaires.<br>
            Permettre aux AESH de travailler 35 heures, ce que font déjà beaucoup d’entre elles en cumulant plusieurs emplois, avec une revalorisation de leur salaire mais sans les intégrer au statut de la Fonction Publique.
            L’accessibilité des universités sera finalisée durant le quinquennat.<br>
            Pôle Emploi sera la porte d’entrée de l’insertion professionnelle et de l’emploi pour toutes les personnes, handicapées ou non, et ce sont ses services qui évalueront les situations et orienteront les personnes.
            Le statut des travailleuses et travailleurs en ESAT sera modifié, avec le droit de se syndiquer et de faire grève, sans pour autant leur donner la qualité de salarié. Mais le chantier reste ouvert avec, nous dit-on, les personnes concernées.
            Chaque personne en situation de handicap qui a un dossier aura un référent MDPH.<br>
            La Prestation de Compensation du Handicap à domicile sera revalorisée sans arriver au zéro reste à charge.<br>
            1,5 milliard d’euros seront débloqués pour améliorer l’accessibilité des établissements recevant du public de proximité (commerces etc).
            Un décret sera pris pour l’accessibilité des lieux de travail.<br>
            Une prise en charge à 100% des fauteuils roulants et des équipements sportifs nécessaires aux athlètes handicapés sera la règle.<br>
            Beaucoup de choses donc, mais sans aborder les moyens financiers à la hauteur des besoins. Certainement, peu d’objectifs seront réalisés et le risque est grand d’alimenter la déception voire la colère des personnes handicapées et de leurs représentants.
            <br>
            Vouloir l’école de la réussite pour tous les enfants demande des moyens humains et financiers que l’Éducation Nationale ne sait pas mettre en œuvre aujourd’hui.<br>
            
            Avoir un référent handicap dans chaque école ne peut pas se faire en comptant seulement sur la bonne volonté des enseignants.<br>
            
            De même, fixer comme horizon le plein emploi pour toutes et tous demande des moyens considérables qui ne peuvent se réduire aux rapprochements des différentes structures.<br>
            
            Permettre aux personnes en ESAT de rejoindre le milieu ordinaire de travail demande un investissement considérable dans une formation qualifiante, en accompagnement
            
            individualisé. Les personnels de l’insertion, eux-mêmes souvent précarisés, auront du mal à le réaliser massivement sachant qu’aujourd’hui, moins de 2% des personnes en ESAT
            
            rejoignent chaque année le milieu ordinaire de travail.<br>
            
            L’accessibilité des lieux recevant du public et celui des transports restent encore à conquérir vu le retard pris par la France dans ces domaines.<br>
            
            L’AMI sera attentive à la mise en œuvre de ce plan et agira avec les personnes concernées, notamment pour l’école, l’insertion professionnelle et l’emploi, afin qu’il soit réalisé et qu’il ne reste pas sans lendemain.
            <br>
            Par notre action collective, avec tous les acteurs engagés sur ces questions, nous redonnerons corps à l’égalité des droits et des chances voulue par la loi du 11 février 2005.<br>
            
            Le Bureau National de l’AMI 2 mai 2005
